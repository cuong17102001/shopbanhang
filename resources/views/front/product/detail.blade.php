<script>
    function change_image(key,userid) {
        var image = <?php echo $product->image ?>;
        var string = '../../public/upload/product/'+userid+'/'+image[key];

        $('#img_330').attr('src',string);
        // console.log(image[0])
        $('#img_zoom').attr('href',string);
    }
    function add_to_cart(){
        var cart = parseInt($('#cart_header').text());
        var qty = $('#qty').val();
        $.ajax({
            method: "POST",
            url: "{{ URL::to('/add-to-cart/'.$product->id) }}",
            data: {
                "_token" : "{{ csrf_token() }}",
                'qty' : qty
            },
            success : function(response){
              console.log(response);
              if(response == 0){
                $('#cart_header').text(cart + 1);
                alert('đã thêm vào giỏ hàng');
              }
              
          }
      });
    }
